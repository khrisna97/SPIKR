// Inspired by blueprintjs
function StyledRadio(props) {
    const classes = useStyles();

    return (
        <Radio
            className={classes.root}
            disableRipple
            color="default"
            checkedIcon={<span className={clsx(classes.icon, classes.checkedIcon)} />}
            icon={<span className={classes.icon} />}
            {...props}
        />
    );
}

export default function CustomizedRadios(prop) {
    const InpProp = {
        onChange:prop.onChange,
        name:prop.name,
        defaultValue:prop.dataSource[0].value ? prop.dataSource[0].value : 0,
    };
    return (
        <FormControl  component="fieldset">
            <RadioGroup {...InpProp} onChange={prop.onChange} row defaultValue="female" aria-label="gender" name="customized-radios">
                {
                    prop.dataSource.map(({value, label}, key)=><FormControlLabel name={prop.name}  key={key} value={value} label={label}  style={{color:"white"}} control={<StyledRadio />} />
                    )
                }

            </RadioGroup>
        </FormControl>
    );
}